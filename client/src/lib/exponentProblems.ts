/** Return a random problem from the pool filtered by difficulty */
export function getRandomExponentProblem(difficulty: Difficulty): ExponentProblem {
  const pool = EXPONENT_PROBLEMS.filter((p) => p.difficulty === difficulty);
  if (pool.length === 0) return EXPONENT_PROBLEMS[0];
  return pool[Math.floor(Math.random() * pool.length)];
}
